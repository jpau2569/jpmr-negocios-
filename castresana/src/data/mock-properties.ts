/* ------------------------------------------------- Actividad del inmueble */

export interface PropertyActivity {
  id: string;
  kind: 'captacion' | 'media' | 'visita' | 'interes' | 'oferta';
  title: string;
  detail?: string;
  at: string; // ISO
}

/**
 * Actividad comercial simulada, determinista a partir de los datos del
 * inmueble (nada aleatorio: los tiempos relativos se mantienen estables).
 * Con Firebase, esto vendrá del historial real.
 */
export function getPropertyActivity(property: Property): PropertyActivity[] {
  const base = new Date(property.publishedAt).getTime();
  const day = 86_400_000;
  const seed = property.id.charCodeAt(2) + property.id.charCodeAt(3);
  const at = (offsetDays: number) => new Date(base + offsetDays * day).toISOString();

  const events: PropertyActivity[] = [
    {
      id: `${property.id}-a1`,
      kind: 'captacion',
      title: 'Captación firmada',
      detail: `Alta en cartera con referencia ${property.reference}.`,
      at: at(0),
    },
    {
      id: `${property.id}-a2`,
      kind: 'media',
      title: `Reportaje fotográfico (${property.photos} fotos)`,
      detail: property.hasVideo ? 'Incluye tour en vídeo.' : undefined,
      at: at(1),
    },
    {
      id: `${property.id}-a3`,
      kind: 'interes',
      title: `${8 + (seed % 9)} guardados en portales`,
      detail: `${30 + (seed % 50)} visualizaciones de ficha esta semana.`,
      at: at(3),
    },
    {
      id: `${property.id}-a4`,
      kind: 'visita',
      title: `${1 + (seed % 3)} visitas realizadas`,
      at: at(5),
    },
  ];

  if (property.status === 'reservado') {
    events.push({
      id: `${property.id}-a5`,
      kind: 'oferta',
      title: 'Oferta aceptada — reservado',
      detail: 'Pendiente de arras.',
      at: at(9),
    });
  }

  return events.reverse();
}
